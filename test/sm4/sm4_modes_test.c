int test_sm4_gcm128(int idx)
{
    unsigned char out[512];
    SIZED_DATA K = gcm128_vectors[idx].K;
    SIZED_DATA IV = gcm128_vectors[idx].IV;
    SIZED_DATA A = gcm128_vectors[idx].A;
    SIZED_DATA P = gcm128_vectors[idx].P;
    SIZED_DATA C = gcm128_vectors[idx].C;
    SIZED_DATA T = gcm128_vectors[idx].T;
    SM4_GCM128_CONTEXT ctx;
    SM4_KEY key;

    MUTABLE_SIZED_DATA tag;
    tag.size = T.size;
    tag.data = malloc(tag.size);

    MUTABLE_SIZED_DATA cipher;
    cipher.size = C.size;
    if (cipher.size == 1)
        cipher.data = NULL;
    else
        cipher.data = malloc(cipher.size);

    /* Size 1 inputs are special-cased to signal NULL. */
    if (A.size == 1)
        A.data = NULL;
    if (P.size == 1)
        P.data = NULL;
    if (C.size == 1)
        C.data = NULL;

    sm4_set_encrypt_key(&key, K.data);

    sm4_gcm128_init(&ctx, &key);
    sm4_gcm128_setiv(&ctx, IV.data, IV.size);
    memset(out, 0, P.size);
    if (A.data != NULL)
        sm4_gcm128_aad(&ctx, A.data, A.size);
    if (P.data != NULL) {
        sm4_gcm128_encrypt(P.data, out, P.size, &ctx, 1);
        memcpy(cipher.data, out, cipher.size);
    }
    // if (!TEST_false(sm4_gcm128_finish(&ctx, T.data, 16))
    //         || (C.data != NULL
    //                 && !TEST_mem_eq(out, P.size, C.data, P.size)))
    //     return 1;
    sm4_gcm128_tag(&ctx, tag.data, tag.size);

    sm4_gcm128_setiv(&ctx, IV.data, IV.size);
    memset(out, 0, P.size);
    if (A.data != NULL)
        sm4_gcm128_aad(&ctx, A.data, A.size);
    if (C.data != NULL)
        sm4_gcm128_encrypt(cipher.data, out, cipher.size, &ctx, 0);
    if (!TEST_false(sm4_gcm128_finish(&ctx, tag.data, 16))
            || (P.data != NULL
                && !TEST_mem_eq(out, P.size, P.data, P.size))) {
//                CRYPTO_gcm128_release(&ctx);
                return 1;
            }
//    CRYPTO_gcm128_release(&ctx);
    return 0;
}
