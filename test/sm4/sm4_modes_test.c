/* only check the encrypted date can decrypt correct*/
int test_sm4_cbc128(int idx)
{
    unsigned char out[512];
    SIZED_DATA K = cbc128_vectors[idx].K;
    SIZED_DATA C_IV = cbc128_vectors[idx].IV;
    SIZED_DATA P = cbc128_vectors[idx].P;
    sm4_key_t key;

    MUTABLE_SIZED_DATA C;
    MUTABLE_SIZED_DATA IV;

    /* Size 1 inputs are special-cased to signal NULL. */
    if (P.size == 1)
        P.data = NULL;

    memset(out, 0, P.size);
    IV.size = C_IV.size;
    IV.data = malloc(IV.size);
    memcpy(IV.data, C_IV.data, IV.size);
    sm4_set_encrypt_key(&key, K.data);

    if (P.data != NULL)
        sm4_cbc_encrypt(P.data, out, P.size, &key, IV.data, 1);

    // if (C.data != NULL
    //     	&& !TEST_mem_eq(out, P.size, C.data, P.size))
    //     return 1;

    IV.size = C_IV.size;
    memcpy(IV.data, C_IV.data, IV.size);
    sm4_set_decrypt_key(&key, K.data);

    if (P.data != NULL) {
        C.size = P.size;
        C.data = malloc(C.size);
        memcpy(C.data, out, C.size);
        memset(out, 0, P.size);
        sm4_cbc_encrypt(C.data, out, C.size, &key, IV.data, 0);
        free(C.data);
    }
    
    free(IV.data);

    if (P.data != NULL
            && !TEST_mem_eq(out, P.size, P.data, P.size))
        return 1;

    return 0;
}

